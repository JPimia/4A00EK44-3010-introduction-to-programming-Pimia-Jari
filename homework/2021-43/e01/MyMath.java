
/**
 * The class MyMath contains methods for basic mathematical operations.
 * 
 * @author Jari Pimia.
 */
public class MyMath {

    /**
     * Checks if number is positive.
     * 
     * This method will check if given integer is positive or negative
     *  and will return true or false depending which one it is.
     * 
     * @param number that is checked.
     * @return true when number is positive, false otherwise.
     */
    public static boolean isPositive(int number) {
        return number > 0;
    }
}
